package com.strong.familynotification.Model;

public class SignalingMessage {

    private String type; // "offer", "answer", "ice-candidate"
    private String senderId;
    private String receiverId;
    private String sdp; // SDP message (for offer/answer)
    private String candidate; // ICE candidate message (if type is "ice-candidate")

    // Getters and Setters
    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getSenderId() {
        return senderId;
    }

    public void setSenderId(String senderId) {
        this.senderId = senderId;
    }

    public String getReceiverId() {
        return receiverId;
    }

    public void setReceiverId(String receiverId) {
        this.receiverId = receiverId;
    }

    public String getSdp() {
        return sdp;
    }

    public void setSdp(String sdp) {
        this.sdp = sdp;
    }

    public String getCandidate() {
        return candidate;
    }

    public void setCandidate(String candidate) {
        this.candidate = candidate;
    }
}